//For <-- laço de repetição ,é quando um bloco de código é execultado enquanto não atender a uma determinada condição.
//E caso a condição não seja atendida o bloco é execultado indefinidamente.
//1Exemplo definido o bloco é execultado até 
for(let casa = 1; casa < 5; casa++){
    console.log(casa)
    console.log("Você comprou uma nova casa")
}
//2Exemplo indefinidamente execultado o bloco é executado
for(let casa = 1; casa < 5; casa--){
    console.log(casa)
    console.log("Você comprou uma nova casa")
}
